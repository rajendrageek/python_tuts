# comments can be used to explain python code
# comments can be used to make the code more readable
# comments can be used to prevent execution when testing code


# creating a comment 
# comments starts with #, and python will ignore them

# this is comment
print("hello, world!")

# comments can be placed at the end of the line,and python will ignore the rest of the line
print('hello, World!') # this is comment

# comments does not have to be text to explain the code, it can also be used to prevent python from executing 


# multiline comments
# python does not really have a syntax for multiline  comments
# to add a multiline comment you could insert a # for each line

# this is a comment 
# written in 
# more than just one line

# Since python ignore string literals that are not assigned to a variable, you can add multiline string(triple quotes) in your code
# and place your comment inside it.


"""this is a comment 
written in 
more than just one line"""


# you can use single quotes too for multiline string

# As long as the string is not assigned to a variable, python will read the code, but then  ignore it and you have made 
# a multiline string 
